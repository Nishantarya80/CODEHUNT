   public int random(int max ,int minm){
       boolean flag=true;
       rand = new Random();
       max=this.max;
       minm=this.minm; 
       int n = rand.nextInt(max) + minm;
        for (int itre=1;itre<=6;itre++){
             if(exc[itre]==n){
                 flag=false;
                 return 0;
             }
       }
       
    
       if(flag==true){
       exc[nextindex]=n;
       ++nextindex;}
          
       return n; 
   }
   
